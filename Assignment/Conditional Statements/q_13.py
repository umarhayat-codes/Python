# Take two numbers and an operator (+, -, *, /) as input and perform the corresponding operation.
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

sum = num1 + num2
print(f"Sum of two number is {sum}!")
sub = num1 - num2
print(f"Subtract of two number is {sub}!")
mul = num1 * num2
print(f"Multiply of two number is {mul}!")
div = num1 / num2
print(f"Division of two number is {div}!")
